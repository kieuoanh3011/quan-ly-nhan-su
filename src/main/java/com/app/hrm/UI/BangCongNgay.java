    void fillTable(String manv) {
        DefaultTableModel model = (DefaultTableModel) tblNgayLam.getModel();
        model.setRowCount(0);

        try {
            LocalDate local = LocalDate.now();
            int nam = local.getYear();
            int thang = local.getMonthValue();
            List<ChamCong> list = dao.selectAllByThangNam(nam, thang, manv);

            for (ChamCong cc : list) {
                Object[] row = {cc.getId(), cc.getMaNV(), cc.getNgay(), cc.getGioiVao(), cc.getPhutVao(), cc.getGioVe(), cc.getPhutVe(), cc.getSoGioLam()};
                System.out.println(cc);
                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

