    private void btnInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInActionPerformed
        StringBuilder sb = new StringBuilder();
        String manv = txtMaNV.getText();
        Vaidation.checkRong(txtMaNV, sb, "Không để trống mã nhân viên");
        if(sb.length()>0){
            MsgBox.alert(this,  sb.toString());
        }
        Date now = new Date();
        LocalDateTime local = LocalDateTime.now();
        int gio = local.getHour();
        int phut = local.getMinute();
        try {
            ChamCong cc = new ChamCong();
            cc.setMaNV(manv);
            cc.setGioiVao(gio);
            cc.setPhutVao(phut);
            cc.setNgay(now);
            dao.insert(cc);
        } catch (Exception e) {
            e.printStackTrace();
        }
        fillTable(manv);
    }//GEN-LAST:event_btnInActionPerformed

    private void btnOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOutActionPerformed
        String manv = txtMaNV.getText();
        Date now = new Date();
        LocalDateTime local = LocalDateTime.now();
        int gio = local.getHour();
        int phut = local.getMinute();
  
        try {
            ChamCong cc = dao.getID(manv, local.getYear(),local.getMonthValue());
            if (cc != null) {
                if(XDate.toString(now).equals(XDate.toString(cc.getNgay()))){
                cc.setGioVe(gio);
                cc.setPhutVe(phut);
                float a = cc.getGioiVao();
                float b = cc.getPhutVao();
                float c = gio;
                float d = phut;
                int tong = (int) ((c + d / 60) - (a + b / 60));
                if(tong<7){
                    cc.setSoGioLam(tong);
                }else{
                    tong = tong -1;
                    cc.setSoGioLam(tong);
                }

                
                dao.update(cc);
                fillTable(manv);
                }else{
                    MsgBox.alert(this, "Bạn chưa check in");
                }
            } else{
                MsgBox.alert(this, "Bạn chưa check in");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnOutActionPerformed

