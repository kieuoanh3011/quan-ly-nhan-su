    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (txtTD.getText().equals("")) {
            MsgBox.alert(this, "Chưa nhập trình độ");
        } else {
            TrinhDo td = new TrinhDo();
            td.setTrinhDo(txtTD.getText());
            try {
                dao.insert(td);
                filltable();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (txtTD.getText().equals("")) {
            MsgBox.alert(this, "Chưa nhập trình độ");
        } else {
            try {
                int id = Integer.parseInt(txtID.getText());
                String mtd = txtTD.getText();
                TrinhDo td = new TrinhDo();
                td.setIdTD(id);
                td.setTrinhDo(mtd);
                dao.update(td);
                filltable();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void tblTDMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTDMouseClicked
        if (evt.getClickCount() == 2) {
            int index = tblTD.getSelectedRow();
            int ma = (int) tblTD.getValueAt(index, 0);
            try {
                TrinhDo td = dao.selectById(ma);
                txtID.setText(String.valueOf(td.getIdTD()));
                txtTD.setText(td.getTrinhDo());
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_tblTDMouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int index = tblTD.getSelectedRow();
        if (index < 0) {
            MsgBox.alert(this, "Chọn trình độ để xóa");
        } else {
            if (MsgBox.confirm(this, "Bạn muốn xóa")) {
                try {
                    int ma = (int) tblTD.getValueAt(index, 0);
                    dao.delete(ma);
                    filltable();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

