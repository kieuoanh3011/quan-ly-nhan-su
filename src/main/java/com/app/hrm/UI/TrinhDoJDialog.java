    void filltable() {
        DefaultTableModel model = (DefaultTableModel) tblTD.getModel();
        model.setRowCount(0);
        try {
            List<TrinhDo> list = dao.selectAll();

            for (TrinhDo td : list) {
                Object[] row = {td.getIdTD(), td.getTrinhDo()};
                model.addRow(row);
            }
        } catch (Exception e) {
        }
    }

