    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        StringBuilder sb = new StringBuilder();
        Vaidation.checkRong(txtTD, sb, "không để trống tên chức vụ\n");
        Vaidation.checkRong(txtBL, sb, "Không để trống bậc lương\n");
        Vaidation.checkSo(txtBL, sb);
        
        if(sb.length()>0){
            MsgBox.alert(this, sb.toString());
            return;
        }
        
            ChucVu td = new ChucVu();
            td.setTenCV(txtTD.getText());
            td.setBacLuong(Double.parseDouble(txtBL.getText()));
            try {
                dao.insert(td);
                filltable();
            } catch (Exception e) {
                e.printStackTrace();
            }
        
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        try {
            int id = Integer.parseInt(txtID.getText());
            String mtd = txtTD.getText();
            ChucVu td = new ChucVu();
            td.setIdCV(id);
            td.setTenCV(mtd);
            td.setBacLuong(Double.parseDouble(txtBL.getText()));
            dao.update(td);
            filltable();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int index = tblTD.getSelectedRow();
        if(index < 0){
            MsgBox.alert(this, "Chọn trình độ để xóa");
        }else{
            if(MsgBox.confirm(this, "Bạn muốn xóa")){
                try{
                    int ma = (int) tblTD.getValueAt(index, 0);
                    dao.delete(ma);
                    filltable();
                }catch(Exception e){
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblTDMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTDMouseClicked
        if(evt.getClickCount() == 2){
            int index = tblTD.getSelectedRow();
            int ma = (int) tblTD.getValueAt(index, 0);
            try {
                ChucVu td = dao.selectById(ma);
                txtID.setText(String.valueOf(td.getIdCV()));
                txtTD.setText(td.getTenCV());
                txtBL.setText(String.valueOf(td.getBacLuong()));
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_tblTDMouseClicked

