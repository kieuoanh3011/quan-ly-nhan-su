    void filltable(){
        DefaultTableModel model = (DefaultTableModel) tblTD.getModel();
        model.setRowCount(0);
        try {
            List<ChucVu> list = dao.selectAll();
            
            for (ChucVu td : list) {
                Object[] row = {td.getIdCV(),td.getTenCV(),td.getBacLuong()};
                model.addRow(row);
            }
        } catch (Exception e) {
        }
    }

